
package Modelo;

public class Modelo_Usuarios {
    
    
    //Nombre de los campos de la tabla usuarios
    private int id_usuario;
    private String nombre_usuario;
    private String domicilio_usuario;
    private String usuario_usuario;
    private String contrasena_usuario;

    
    
    public int getId_usuario() {
        return id_usuario;
    }

    public void setId_usuario(int id_usuario) {
        this.id_usuario = id_usuario;
    }

    public String getNombre_usuario() {
        return nombre_usuario;
    }

    public void setNombre_usuario(String nombre_usuario) {
        this.nombre_usuario = nombre_usuario;
    }

    public String getDomicilio_usuario() {
        return domicilio_usuario;
    }

    public void setDomicilio_usuario(String domicilio_usuario) {
        this.domicilio_usuario = domicilio_usuario;
    }

    public String getUsuario_usuario() {
        return usuario_usuario;
    }

    public void setUsuario_usuario(String usuario_usuario) {
        this.usuario_usuario = usuario_usuario;
    }

    public String getContrasena_usuario() {
        return contrasena_usuario;
    }

    public void setContrasena_usuario(String contrasena_usuario) {
        this.contrasena_usuario = contrasena_usuario;
    }

   
    
    
    
}
